extern "C"
{
#include <libavformat/avformat.h>
#include <libavcodec/avcodec.h>
#include <libswscale/swscale.h>
#include <libavutil/imgutils.h>
}

#include <iostream>

int main()
{
    const char* url = getenv("HTTP_STREAM_URL"); // MJPEG stream

    AVFormatContext* fmt_ctx = nullptr;

    // 1. Open HTTP stream
    if (avformat_open_input(&fmt_ctx, url, nullptr, nullptr) < 0) {
        std::cerr << "Could not open stream\n";
        return -1;
    }

    if (avformat_find_stream_info(fmt_ctx, nullptr) < 0) {
        std::cerr << "Could not find stream info\n";
        return -1;
    }

    // 2. Find video stream
    int video_stream_index = -1;
    for (unsigned i = 0; i < fmt_ctx->nb_streams; i++) {
        if (fmt_ctx->streams[i]->codecpar->codec_type == AVMEDIA_TYPE_VIDEO) {
            video_stream_index = i;
            break;
        }
    }

    if (video_stream_index == -1) {
        std::cerr << "No video stream found\n";
        return -1;
    }

    // 3. Setup decoder
    AVCodecParameters* codecpar = fmt_ctx->streams[video_stream_index]->codecpar;
    const AVCodec* codec = avcodec_find_decoder(codecpar->codec_id);

    AVCodecContext* codec_ctx = avcodec_alloc_context3(codec);
    avcodec_parameters_to_context(codec_ctx, codecpar);

    if (avcodec_open2(codec_ctx, codec, nullptr) < 0) {
        std::cerr << "Could not open codec\n";
        return -1;
    }

    // 4. Allocate frames
    AVFrame* frame = av_frame_alloc();      // decoded (YUV)
    AVFrame* rgb_frame = av_frame_alloc();  // converted (RGB)

    int width = codec_ctx->width;
    int height = codec_ctx->height;

    // Allocate RGB buffer
    int num_bytes = av_image_get_buffer_size(AV_PIX_FMT_RGB24, width, height, 1);
    uint8_t* buffer = (uint8_t*)av_malloc(num_bytes);

    av_image_fill_arrays(rgb_frame->data, rgb_frame->linesize,
                         buffer, AV_PIX_FMT_RGB24, width, height, 1);

    // 5. Setup scaler (YUV → RGB)
    SwsContext* sws_ctx = sws_getContext(
        width, height, codec_ctx->pix_fmt,
        width, height, AV_PIX_FMT_RGB24,
        SWS_BILINEAR, nullptr, nullptr, nullptr
    );

    AVPacket* pkt = av_packet_alloc();

    std::cout << "Streaming...\n";

    // 6. Read + decode loop

    // Init GLFW
    glfwInit();
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

    GLFWwindow* window = glfwCreateWindow(1920, 1080, "Hello Triangle", NULL, NULL);
    if (!window) {
        std::cout << "Failed to create GLFW window\n";
        glfwTerminate();
        return -1;
    }
    glfwMakeContextCurrent(window);
    IMGUI_CHECKVERSION();
    ImGui::CreateContext();
    ImGuiIO& io = ImGui::GetIO();
    io.ConfigFlags |= ImGuiConfigFlags_NavEnableKeyboard;     // Enable Keyboard Controls
    io.ConfigFlags |= ImGuiConfigFlags_NavEnableGamepad;      // Enable Gamepad Controls

    // Setup Platform/Renderer backends
    ImGui_ImplGlfw_InitForOpenGL(window, true);          // Second param install_callback=true will install GLFW callbacks and chain to existing ones.
    ImGui_ImplOpenGL3_Init();

    GLuint videoTex = 0;
    glGenTextures(1, &videoTex);
    glBindTexture(GL_TEXTURE_2D, videoTex);

    // Allocate storage (no data yet)
    glTexImage2D(GL_TEXTURE_2D,
                0,                // mip level
                GL_RGB8,          // internal format (on GPU)
                width,
                height,
                0,
                GL_RGB,           // input format
                GL_UNSIGNED_BYTE,
                nullptr);         // no data yet

    // Set filtering
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);

    // Important for video (avoid wrapping artifacts)
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

    // VERY IMPORTANT: alignment fix
    glPixelStorei(GL_UNPACK_ALIGNMENT, 1);

    while (!glfwWindowShouldClose(window)) {
        glfwPollEvents();
        glfwMakeContextCurrent(window);
        glClear(GL_COLOR_BUFFER_BIT);

        if (av_read_frame(fmt_ctx, pkt) >= 0) {
            if (pkt->stream_index == video_stream_index) {
                if (avcodec_send_packet(codec_ctx, pkt) == 0) {

                    while (avcodec_receive_frame(codec_ctx, frame) == 0) {

                        // Convert to RGB
                        sws_scale(sws_ctx,
                                frame->data, frame->linesize,
                                0, height,
                                rgb_frame->data, rgb_frame->linesize);

                        // 👉 THIS is your raw frame buffer
                        uint8_t* rgb_data = rgb_frame->data[0];

                        // Example: print first pixel
                        std::cout << "Frame received. First pixel: "
                                << (int)rgb_data[0] << ", "
                                << (int)rgb_data[1] << ", "
                                << (int)rgb_data[2] << "\n";

                        // 👉 Here you would upload to GPU texture
                        glBindTexture(GL_TEXTURE_2D, videoTex);
                        glTexSubImage2D(GL_TEXTURE_2D, 0, 0, 0, width, height, GL_RGB, GL_UNSIGNED_BYTE, rgb_data);
                    }
                }
            }
            av_packet_unref(pkt);
        }
        else
        {
            // failed to get frame  
            break;
        }


        ImGui_ImplOpenGL3_NewFrame();
        ImGui_ImplGlfw_NewFrame();
        ImGui::NewFrame();
        ImGui::ShowDemoWindow(); // Show demo window! :)

        ImGui::Begin("Video");

        ImGui::Image((ImTextureID)(intptr_t)videoTex,
                    ImVec2(width, height));

        ImGui::End();

        ImGui::Render();
        ImGui_ImplOpenGL3_RenderDrawData(ImGui::GetDrawData());
        glfwSwapBuffers(window);
    }

    // Cleanup
    av_free(buffer);
    av_frame_free(&frame);
    av_frame_free(&rgb_frame);
    av_packet_free(&pkt);
    sws_freeContext(sws_ctx);
    avcodec_free_context(&codec_ctx);
    avformat_close_input(&fmt_ctx);
    ImGui_ImplOpenGL3_Shutdown();
    ImGui_ImplGlfw_Shutdown();
    ImGui::DestroyContext();
}
